#example1 The Python print() function is often used to output variables.
x = "Python is awesome"
print(x)
#example2 In the print() function, you output multiple variables, separated by a comma:
x = "Python"
y = "is"
z = "awesome"
print(x, y, z)
#example3 You can also use the + operator to output multiple variables:
x = "Python "
y = "is "
z = "awesome"
print(x + y + z)
#example4 For numbers, the + character works as a mathematical operator:
x = 5
y = 10
print(x + y)
#example5 The best way to output multiple variables in the print() function is to separate them with commas, which even support different data types:
x = 5
y = "John"
print(x, y)